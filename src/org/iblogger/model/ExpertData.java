public class ExpertData {

	private int factorCode;
	
	private int expertCode;
	
	private float weightLevel;

	public int getFactorCode() {
		return factorCode;
	}

	public void setFactorCode(int factorCode) {
		this.factorCode = factorCode;
	}

	public int getExpertCode() {
		return expertCode;
	}

	public void setExpertCode(int expertCode) {
		this.expertCode = expertCode;
	}

	public float getWeightLevel() {
		return weightLevel;
	}

	public void setWeightLevel(float weightLevel) {
		this.weightLevel = weightLevel;
	}
	
}
